#include <stdio.h>
#include "main.h"
/**
 * _isdigit - prototype function that checks for a digit(0 through 9)
 * @c: contains digit to be checked
 * Returns 1 if c is a digit
 * Returns 0 otherwise
 * Returns: 0
 */
int _isdigit(int c)
{
	int result = if (c >= '0' && c <= '9');
	return (result);
}
